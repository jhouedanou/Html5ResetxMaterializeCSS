//      $img = wp_get_attachment_image_src($id, 'my-custom-image-size');
        $fulluma =  wp_get_attachment_image_src($id, 'large');
        $img = wp_get_attachment_image_src($id, 'large');
        $thumb = wp_get_attachment_image_src($id, 'medium');
